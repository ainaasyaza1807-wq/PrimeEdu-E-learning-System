    // The previous screen will call this method to pass the subject code over!
    public void setupScreen(String subjectCode) {
        this.currentSubjectCode = subjectCode;
        lblTitle.setText("Managing Materials for: " + subjectCode);
        loadTable();
    }

    // This method rebuilds the table showing ONLY materials for this subject
    private void loadTable() {

    MaterialController controller = new MaterialController();

    java.util.ArrayList<Material> allMaterials =
            controller.getMaterialList();

    String[] columns = {
        "Instructor",
        "Standard",
        "File Path"
    };

    javax.swing.table.DefaultTableModel model =
            new javax.swing.table.DefaultTableModel(columns, 0);

    for (Material m : allMaterials) {

        if (m.getSubjectCode().equals(currentSubjectCode)) {

            String[] rowData = {
                m.getInstructorName(),
                m.getStandard(),
                m.getFilePath()
            };

            model.addRow(rowData);
        }
    }

    tblMaterials.setModel(model);
}
