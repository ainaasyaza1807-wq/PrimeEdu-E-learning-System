    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
       // 1. Find which row they clicked
        int selectedRow = tblMaterials.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a material from the table to open.");
            return;
        }

        // 2. Grab the file path from column 2 (Instructor is 0, Standard is 1, Path is 2)
        String filePath = tblMaterials.getValueAt(selectedRow, 2).toString();

        // 3. Command the Operating System to open it!
        try {
            java.io.File file = new java.io.File(filePath);
            if (file.exists() && java.awt.Desktop.isDesktopSupported()) {
                java.awt.Desktop.getDesktop().open(file);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: The file could not be found. It may have been moved or deleted.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error opening file: " + e.getMessage());
        }
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // 1. Find which row they clicked
        int selectedRow = tblMaterials.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a material from the table to delete.");
            return;
        }

        String filePath = tblMaterials.getValueAt(selectedRow, 2).toString();

        // 2. Ask for confirmation before deleting permanently!
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this material?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            // 3. Send it to the controller
            MaterialController controller = new MaterialController();
            controller.deleteMaterial(currentSubjectCode, filePath);
            
            // 4. Instantly refresh this table to show it's gone
            loadTable();
            javax.swing.JOptionPane.showMessageDialog(this, "Material successfully deleted!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

