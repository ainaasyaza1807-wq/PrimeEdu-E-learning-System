    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // 1. Figure out which row the user clicked on in the table
        int selectedRow = tblSubjects.getSelectedRow();
        
        // If it equals -1, it means they didn't click on anything!
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please click on a subject in the table to delete it.");
            return;
        }
        
        // 2. Grab the Subject Code from Column 0 of the clicked row
        String subjectCode = tblSubjects.getValueAt(selectedRow, 0).toString();
        
        // 3. Double-check with the user (Always safe before deleting!)
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete " + subjectCode + "?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            
            // 4. Send the code to our Controller to delete from memory & text file
            SubjectController controller = new SubjectController();
            controller.deleteSubject(subjectCode);
            
            // 5. Visually remove the row from the table right now
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblSubjects.getModel();
            model.removeRow(selectedRow);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Subject deleted successfully!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void refreshTable(String standardFilter, String searchKeyword) {

    SubjectController controller = new SubjectController();

    java.util.ArrayList<Subject> allSubjects =
            controller.getSubjectList();

    String[] columns = {
        "Subject Code",
        "Subject Name",
        "Standard",
        "Teacher"
    };

    javax.swing.table.DefaultTableModel model =
            new javax.swing.table.DefaultTableModel(columns, 0);

    String searchLower = searchKeyword.toLowerCase();

    for (Subject s : allSubjects) {

        boolean matchesStandard =
                standardFilter.equals("All Standards")
                || s.getStandard().equals(standardFilter);

        boolean matchesSearch =
                searchLower.isEmpty()
                || s.getName().toLowerCase().contains(searchLower)
                || s.getCode().toLowerCase().contains(searchLower);

        if (matchesStandard && matchesSearch) {

            String[] rowData = {
                s.getCode(),
                s.getName(),
                s.getStandard(),
                s.getTeacherName()
            };

            model.addRow(rowData);
        }
    }

    tblSubjects.setModel(model);
}
    
