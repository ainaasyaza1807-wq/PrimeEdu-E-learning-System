    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // 1. Find out which row they clicked
        int selectedRow = tblSubjects.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a subject in the table to edit.");
            return;
        }

        // 2. Grab the current Code and Name from that row
        String code = tblSubjects.getValueAt(selectedRow, 0).toString();
        String currentName = tblSubjects.getValueAt(selectedRow, 1).toString();

        // 3. Pop-up to ask for the New Name
        String newName = javax.swing.JOptionPane.showInputDialog(this, "Enter a new name for " + code + ":", currentName);
        
        // If they click 'Cancel' or leave it blank, stop the process
        if (newName == null || newName.trim().isEmpty()) { return; } 

        // 4. Pop-up dropdown to ask for the New Standard
        String[] standards = {"Std 4", "Std 5", "Std 6"};
        String newStandard = (String) javax.swing.JOptionPane.showInputDialog(this, "Select the new standard:", "Update Standard", javax.swing.JOptionPane.QUESTION_MESSAGE, null, standards, standards[0]);
        
        if (newStandard == null) { return; }
        // Grab the current teacher name from column 3 (Code is 0, Name is 1, Standard is 2, Teacher is 3)
        String currentTeacher = tblSubjects.getValueAt(selectedRow, 3).toString();

        // Pop-up to ask for the New Teacher
        String newTeacher = javax.swing.JOptionPane.showInputDialog(this, "Enter a new teacher name:", currentTeacher);
        if (newTeacher == null || newTeacher.trim().isEmpty()) { return; } 

        // Send ALL the new data to the Controller! (Make sure you pass all 4 things)
        SubjectController controller = new SubjectController();
        controller.updateSubject(code, newName, newStandard, newTeacher);
        

        // 6. Refresh the table instantly to show the changes
        refreshTable(cmbFilter.getSelectedItem().toString(), txtSearch.getText());
        javax.swing.JOptionPane.showMessageDialog(this, "Subject updated successfully!");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnOpenMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenMaterialActionPerformed
        // 1. Figure out which subject they clicked
        int selectedRow = tblSubjects.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a subject in the table to manage its materials.");
            return;
        }

        String code = tblSubjects.getValueAt(selectedRow, 0).toString();

        // 2. Create the new pop-up screen
        ManageMaterialsUI manageScreen = new ManageMaterialsUI();
        
        // 3. Send the selected subject code over to the new screen using our custom method!
        manageScreen.setupScreen(code);
        
        // 4. Make it visible
        manageScreen.setVisible(true);
    }//GEN-LAST:event_btnOpenMaterialActionPerformed

