    private void loadAssessmentData() {
        // Jika tiada ID dihantar (panggilan kosong), set nilai lalai ringkas
        if (this.assessmentId == null || this.assessmentId.isEmpty()) {
            lblAssignmentName.setText("Assessment: NO ASSESSMENT SELECTED");
            lblFileNameDisplay.setText("No file received.");
            txtFilePathDisplay.setText("");
            return;
        }
        
        AssessmentController controller = new AssessmentController();
        java.util.List<Assessment> allAssessments = controller.getAllAssessments();
        Assessment activeRecord = null;
        
        if (allAssessments != null) {
            for (Assessment ass : allAssessments) {
                if (ass.getAssessmentId() != null && ass.getAssessmentId().equalsIgnoreCase(this.assessmentId)) {
                    activeRecord = ass;
                    break;
                }
            }
        }
        
        if (activeRecord != null) {
        lblAssignmentName.setText("Assessment: " + activeRecord.getTitle().toUpperCase());
    
        String desc = activeRecord.getDescription(); 
        lblFileNameDisplay.setText("Record Data: " + desc);
   
        String studentFileName = "Assessments.txt"; // Fallback default
        if (desc != null && desc.contains("Student Submission:")) {
        // Ekstrak nama fail selepas perkataan "Student Submission: "
        studentFileName = desc.substring(desc.indexOf("Student Submission:") + 19).trim();
    }
    
    txtFilePathDisplay.setText(studentFileName);
    this.studentFile = new java.io.File(studentFileName);
        } else {
            lblFileNameDisplay.setText("No data found.");
            txtFilePathDisplay.setText("");
        }
    }
