    private void btnSaveGradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveGradeActionPerformed
    String id = this.assessmentId; 
        
        // Validasi 1: Pastikan ID tidak kosong/null
        if (id == null || id.trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "System Error: Assessment ID is missing!\nPlease open this frame from the Records Table.", 
                "System Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validasi 2: Ambil input markah dari jTextField2 dan pastikan tidak kosong
        String scoreText = jTextField2.getText().trim();
        if (scoreText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a student score first!", "Validation Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Tukar String markah kepada Integer dengan selamat
        int gred = 0;
        try {
            gred = Integer.parseInt(scoreText);
            
            // Validasi julat markah (0 - 100)
            if (gred < 0 || gred > MAX_SCORE) {
                javax.swing.JOptionPane.showMessageDialog(this, "Score must be between 0 and 100!", "Validation Error", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Score must be a valid number!", "Validation Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 3. Ambil nilai komen daripada 'txtCommentInput' (Sebab nama variable anda txtCommentInput)
        String komen = txtCommentInput.getText().trim(); 
        
        // 4. Panggil Controller untuk hantar data ke database MySQL
        controller.AssessmentController assessmentController = new controller.AssessmentController();
        boolean success = assessmentController.saveAssessmentGrade(id, gred, komen);
        
        // 5. Papar status kejayaan/kegagalan kepada guru
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Grade (" + gred + "/100) and feedback saved successfully to database!", 
                "Success", 
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
            // Buka semula jadual rekod yang telah dikemas kini dan tutup skrin gred ini
            new AssessmentRecodsFrame().setVisible(true);
            this.dispose(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Database Error: Failed to update records.\n(Tip: Ensure Assessment ID '" + id + "' matches 'assessment_id' column in phpMyAdmin)", 
                "System Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveGradeActionPerformed

