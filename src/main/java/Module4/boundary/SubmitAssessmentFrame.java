    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int response = fileChooser.showOpenDialog(this);
        
        if (response == JFileChooser.APPROVE_OPTION) {
            studentAttachedFile = fileChooser.getSelectedFile();
            lblFilePath.setText(studentAttachedFile.getName());
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

    private void btnSubmitAssessmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitAssessmentActionPerformed
        if (studentAttachedFile == null || !studentAttachedFile.exists()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please browse and select a valid file first!");
            return;
        }

        AssessmentController controller = new AssessmentController();
        
        String submissionInfo = "Student Submission: " + studentAttachedFile.getName();
        
        boolean success = controller.updateAssessmentDescription(this.assessmentId, submissionInfo);
        
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "File " + studentAttachedFile.getName() + " successfully uploaded and submitted!",
                "Submission Success",
                JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "System Error: Failed to record your file submission.", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitAssessmentActionPerformed

