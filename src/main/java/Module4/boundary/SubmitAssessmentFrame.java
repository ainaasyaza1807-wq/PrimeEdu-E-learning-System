    private void loadActiveAssessment() {
        if (this.assessmentId == null || String.valueOf(this.assessmentId).isEmpty()) {
            lblAssessmentTitle.setText("NO ASSESSMENT SELECTED");
            return;
        }

        AssessmentController controller = new AssessmentController();
        java.util.List<Assessment> allAssessments = controller.getAllAssessments();
        Assessment activeRecord = null;

        if (allAssessments != null) {
            for (Assessment ass : allAssessments) {
                if (ass.getAssessmentId() != null && ass.getAssessmentId().equalsIgnoreCase(this.assessmentId)) {
                    activeRecord = ass;
                    break;
                }
            }
        }

        if (activeRecord != null) {
            lblAssessmentTitle.setText(activeRecord.getTitle().toUpperCase());
            jTextField1.setText(activeRecord.getAssessmentId());
            jTextField1.setEditable(false);
            
            this.assessmentDueDate = activeRecord.getDueDate(); 
            
            if (this.assessmentDueDate != null) {
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
                lblDueDateDisplay.setText("Due Date: " + sdf.format(this.assessmentDueDate));
            }
        } else {
            lblAssessmentTitle.setText("ASSESSMENT: " + this.assessmentId.toUpperCase());
            jTextField1.setText(this.assessmentId);
            jTextField1.setEditable(false);
        }
        
        checkDueDateRestriction();
    }
    
    private void checkDueDateRestriction() {
        Date today = new Date(); 
        
        if (assessmentDueDate != null && today.after(assessmentDueDate)) {
            JOptionPane.showMessageDialog(this, 
                "The due date for this assessment has passed!\nYou can no longer upload or submit any files.", 
                "Submission Closed", 
                JOptionPane.ERROR_MESSAGE);
            
            btnBrowse.setEnabled(false);
            btnSubmitAssessment.setEnabled(false);
            lblFilePath.setText("SUBMISSION CLOSED (LATE)");
        }
    }
    
