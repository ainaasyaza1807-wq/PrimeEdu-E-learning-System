    private void populateTable() {
    // 1. Dapatkan model jadual JTable anda
    // Sila tukar 'jTable1' mengikut Nama Variable JTable sebenar anda di mod Design
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    
    // 2. Bersihkan jadual daripada sebarang data lama (supaya data tidak bertindih)
    model.setRowCount(0);
    
    // 3. Panggil Controller untuk ambil semua rekod dari database
    controller.AssessmentController controller = new controller.AssessmentController();
    java.util.ArrayList<entity.Assessment> list = controller.getAllAssessments();
    
    // 4. Masukkan data ke dalam jadual baris demi baris
    if (list != null) {
        for (entity.Assessment ass : list) {
            Object[] row = new Object[4];
            row[0] = ass.getAssessmentId();
            row[1] = ass.getTitle();
            row[2] = (ass.getInstructorId() != null) ? ass.getInstructorId() : "N/A";
            row[3] = (ass.getScore() >= 0) ? ass.getScore() + " / 100" : "PENDING";
            
            model.addRow(row);
        }
    } else {
            System.out.println("Log: Tiada data atau senarai assessment kosong dari database.");
        }
}
    
    private void populateTableData() {

    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        AssessmentController assessmentCtrl = new AssessmentController();
        java.util.List<entity.Assessment> allAssessments = assessmentCtrl.getAllAssessments();

        System.out.println("DEBUG: MySQL Database assessment size is currently: " + allAssessments.size());

        if (allAssessments == null || allAssessments.isEmpty()) {
            System.out.println("Status: No records found inside Text File Database.");
            return;
        }

        int index = 1;
        for (entity.Assessment record : allAssessments) {
            String fileName = "Submission_" + record.getAssessmentId() + ".pdf"; 
            String currentScore = "Pending Grading";
            
            // KOD RASMI: Ekstrak markah daripada description jika sudah digred
            String desc = record.getDescription();
            if (desc != null && desc.contains("| Marks:")) {
                try {
                    // Mengambil data selepas perkataan "| Marks: " sehingga "/"
                    int start = desc.indexOf("| Marks:") + 9;
                    int end = desc.indexOf("/100");
                    if (end > start) {
                        currentScore = desc.substring(start, end).trim() + " / 100";
                    }
                } catch (Exception e) {
                    currentScore = "Graded";
                }
            }

            Object[] rowData = {
                record.getAssessmentId(), // Menggunakan ID sebenar sebagai rujukan baris
                record.getTitle(),
                fileName,
                currentScore
            };
            model.addRow(rowData);
            index++;
        }
}
    
