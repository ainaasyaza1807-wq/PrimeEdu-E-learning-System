    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "PLEASE SELECT ANY RECORDS IN THE TABLE TO BE DELETED!");
        return;
    }
    
    // 1. Ambil ID Assessment dari baris yang dipilih (Kolum 0)
    String selectedId = jTable1.getValueAt(selectedRow, 0).toString();
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Do you want to delete assessment " + selectedId + " from Database?", "DELETION CONFIRM", 
            javax.swing.JOptionPane.YES_NO_OPTION);
            
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        // Konfigurasi sambungan database port 3307 anda
        String url = "jdbc:mysql://localhost:3307/primeedu_db";
        String user = "root";
        String password = "";
        
        // Arahan SQL untuk padam rekod berdasarkan ID
        String sql = "DELETE FROM assessments WHERE assessment_id = ?";
        
        try (java.sql.Connection conn = java.sql.DriverManager.getConnection(url, user, password);
             java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            pstmt.setString(1, selectedId);
            int rowsDeleted = pstmt.executeUpdate();
            
            if (rowsDeleted > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "RECORD DELETION SUCCESSFUL FROM DATABASE!");
                populateTableData(); // Refresh jadual terus secara automatik
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Record not found in database.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

