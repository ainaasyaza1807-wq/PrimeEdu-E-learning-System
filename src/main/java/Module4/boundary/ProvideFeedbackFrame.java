    private void btnSubmitFeedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitFeedbackActionPerformed
        String feedbackText = txtFeedbackArea.getText().trim();
        
        if (feedbackText.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "Validation Error: Feedback comment cannot be empty!\nPlease write a valid comment before submitting.", 
                    "Empty Feedback", 
                    JOptionPane.ERROR_MESSAGE);
            return; 
        }
        
        if (feedbackText.length() > MAX_CHAR_LIMIT) {
            JOptionPane.showMessageDialog(this, 
                    "Validation Error: Your feedback exceeds the maximum allowed limit of " + MAX_CHAR_LIMIT + " characters!", 
                    "Character Limit Exceeded", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        java.io.File feedbackFile = new java.io.File("learning_feedback.txt");
       
        try (java.io.FileWriter fw = new java.io.FileWriter(feedbackFile, true);
             java.io.BufferedWriter bw = new java.io.BufferedWriter(fw)) {
            
            String timestamp = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date());
           
            String cleanFeedback = feedbackText.replace("\n", " ").replace(",", ";"); 
            
            String recordLine = timestamp + " | " + this.currentAssessmentTitle + " | " + cleanFeedback;
            
            bw.write(recordLine);
            bw.newLine(); 
            bw.flush();
            
            JOptionPane.showMessageDialog(this, 
                    "Feedback stored and submitted successfully to '" + feedbackFile.getName() + "'!\nIt is now visible to students, parents, and teachers.", 
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
           
            this.dispose();
            
        } catch (java.io.IOException ex) {
            JOptionPane.showMessageDialog(this, 
                    "System File Error: Failed to save feedback to text file.\n" + ex.getMessage(), 
                    "I/O Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitFeedbackActionPerformed

