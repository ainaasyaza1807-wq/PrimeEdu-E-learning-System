    private void setupCharacterCounter() {
        txtFeedbackArea.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) { updateCount(); }
            @Override
            public void removeUpdate(DocumentEvent e) { updateCount(); }
            @Override
            public void changedUpdate(DocumentEvent e) { updateCount(); }
            
            private void updateCount() {
                int length = txtFeedbackArea.getText().length();
                lblCharCount.setText("Characters: " + length + " / " + MAX_CHAR_LIMIT);
                
                // Tukar warna teks jadi merah jika lebih had limit
                if (length > MAX_CHAR_LIMIT) {
                    lblCharCount.setForeground(java.awt.Color.RED);
                } else {
                    lblCharCount.setForeground(new java.awt.Color(102, 102, 102));
                }
            }
        });
    }
